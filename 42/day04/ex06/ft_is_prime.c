int		ft_is_prime(int nb)
{
	int		count;

	count = 3;
	if (nb <= 1)
		return (0);
	if (nb % 2 == 0 && nb > 2)
		return (0);
	while (count < nb / 2)
	{
		if (nb % count == 0)
			return (0);
		count += 2;
	}
	return (1);
}
